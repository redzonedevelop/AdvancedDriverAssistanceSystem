/* 초기화 */
void motor_init(){
    gtm_clock_init();
    motor_timer_init();

}

void gtm_clock_init() {
    IfxGtm_enable(&MODULE_GTM);
    IfxGtm_Cmu_setClkFrequency(&MODULE_GTM, IfxGtm_Cmu_Clk_0, CLK_FREQ);
    IfxGtm_Cmu_enableClocks(&MODULE_GTM, IFXGTM_CMU_CLKEN_CLK0);
}

void motor_timer_init() {
    for (int i = 0; i < MOTOR_MAX_CH; ++i) {
        IfxGtm_Atom_Timer_Config config;
        IfxGtm_Atom_Timer_initConfig(&config, &MODULE_GTM);

        config.atom = atomPins[i]->atom;
        config.timerChannel = atomPins[i]->channel;
        config.clock = IfxGtm_Cmu_Clk_0;
        config.base.frequency = 0;
        config.base.trigger.enabled = TRUE;
        config.base.trigger.outputEnabled = TRUE;
        config.base.trigger.outputMode = IfxPort_OutputMode_pushPull;
        config.base.trigger.outputDriver = IfxPort_PadDriver_cmosAutomotiveSpeed1;
        config.base.trigger.risingEdgeAtPeriod = TRUE;
        config.base.trigger.triggerPoint = (uint32)(PULSE_US / 1e6f * CLK_FREQ);
        config.base.countDir = IfxStdIf_Timer_CountDir_up;
        config.initPins = TRUE;
        config.triggerOut = atomPins[i];
        config.base.isrPriority = i + 10;
        config.base.isrProvider = IfxSrc_Tos_cpu0;


        // Initialize timer
        IfxGtm_Atom_Timer_init(&atomTimers[i], &config);
        IfxGtm_Atom_Timer_setSingleMode(&atomTimers[i], FALSE);

        // Enable output and channel manually (recommended)
        uint16 mask = 1 << config.timerChannel;
        IfxGtm_Atom_Agc_enableChannels(atomTimers[i].agc, mask, 0, TRUE);
        IfxGtm_Atom_Agc_enableChannelsOutput(atomTimers[i].agc, mask, 0, TRUE);

        // Set Compare1 (trigger point) again to ensure PWM works
        IfxGtm_Atom_Timer_setTrigger(&atomTimers[i], config.base.trigger.triggerPoint);

        // Interrupt (Compare0)
        IfxGtm_Atom_Ch_setNotification(atomTimers[i].atom, config.timerChannel, IfxGtm_IrqMode_level, TRUE, FALSE);
        volatile Ifx_SRC_SRCR* src = IfxGtm_Atom_Ch_getSrcPointer(&MODULE_GTM, config.atom, config.timerChannel);
        IfxSrc_init(src, config.base.isrProvider, config.base.isrPriority);
        IfxSrc_enable(src);
    }

    // Apply update and run
    for (int i = 0; i < MOTOR_MAX_CH; ++i)
        IfxGtm_Atom_Timer_addToChannelMask(&atomTimers[0], atomPins[i]->channel);

}

void motor_start()
{
    IfxGtm_Atom_Timer_run(&atomTimers[0]);
    IfxGtm_Atom_Timer_applyUpdate(&atomTimers[0]);
    motor_set_all_speed(1);
}

void motor_set_all_speed(uint32_t freq_hz)
{
    uint16 channelMask = 0;

    for (int i = 0; i < MOTOR_MAX_CH; ++i)
    {

        if (freq_hz == 0)
            continue;

        Ifx_TimerValue period = (Ifx_TimerValue)(CLK_FREQ / freq_hz);
        Ifx_TimerValue trigger = (Ifx_TimerValue)((PULSE_US * CLK_FREQ) / 1000000);

        // 유효성 보장
        if (trigger >= period) trigger = period - 1;

        // 채널별 설정
        IfxGtm_Atom_Timer_disableUpdate(&atomTimers[i]);

        IfxGtm_Atom_Timer_setPeriod(&atomTimers[i], period);           // sets Compare1
        IfxGtm_Atom_Timer_setTrigger(&atomTimers[i], trigger);         // sets Compare1 (output)


        // 마스크에 현재 채널 추가
        channelMask |= (1 << atomPins[i]->channel);
    }

    // 동기 업데이트: 모든 채널 force update 후 trigger
    Ifx_GTM_ATOM_AGC* agc = atomTimers[0].agc;
    IfxGtm_Atom_Agc_setChannelsForceUpdate(agc, channelMask, 0, 0, 0);
    IfxGtm_Atom_Agc_trigger(agc);  // 모든 채널 동시에 업데이트
}

void motor_update_speed(uint8_t id, uint32_t freq_hz)
{
    if (freq_hz == 0) return;

    Ifx_TimerValue period = CLK_FREQ / freq_hz;
    Ifx_TimerValue trigger = (PULSE_US * CLK_FREQ) / 1000000;

    if (trigger >= period)
        trigger = period - 1;

    IfxGtm_Atom_Timer_disableUpdate(&atomTimers[id]);

IfxGtm_Atom_Timer_setPeriod(&atomTimers[id], period);
    IfxGtm_Atom_Timer_setTrigger(&atomTimers[id], trigger);

    uint16 mask = 1 << atomPins[id]->channel;
    IfxGtm_Atom_Agc_setChannelsForceUpdate(atomTimers[id].agc, mask, 0, 0, 0);
    IfxGtm_Atom_Agc_trigger(atomTimers[id].agc);
}

void motor_update_target(uint32_t new_target_freq[MOTOR_MAX_CH], uint8_t new_accel)
{
    for (int i = 0; i < MOTOR_MAX_CH; ++i)
    {
        target_freq[i] = new_target_freq[i];
        motor_state[i] = (target_freq[i] > accel_table[accel_index[i]])
                         ? MOTOR_STATE_ACCEL
                         : MOTOR_STATE_DECEL;
    }
    accel = new_accel;
}


void handle_motor_isr(uint8_t id)
{
    IfxGtm_Atom_Timer_acknowledgeTimerIrq(&atomTimers[id]);

    switch (motor_state[id])
    {
        case MOTOR_STATE_ACCEL:
            if (accel_table[accel_index[id]] < target_freq[id])
            {
                accel_index[id] += accel;
                if (accel_index[id] >= TABLE_LEN)
                {
                    accel_index[id] = TABLE_LEN - 1;
                    motor_state[id] = MOTOR_STATE_CONST;
                }
            }
            else
            {
                motor_state[id] = MOTOR_STATE_CONST;
            }
            break;

        case MOTOR_STATE_DECEL:
            if (accel_index[id] > accel)
            {
                accel_index[id] -= accel;
            }
            else
            {
                accel_index[id] = 0;
                motor_state[id] = MOTOR_STATE_STOP;
            }
            break;

        default:
            break;
    }

    uint32_t next_freq = accel_table[accel_index[id]];
    motor_update_speed(id, next_freq);
}

void atom0_isr(void) { handle_motor_isr(0); }
void atom2_isr(void) { handle_motor_isr(1); }
void atom4_isr(void) { handle_motor_isr(2); }
void atom6_isr(void) { handle_motor_isr(3); }
