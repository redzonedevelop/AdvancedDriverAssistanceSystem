#define CLK_FREQ   1000000.0f  // CMU_CLK0: 1MHz
#define PWM_FREQ   500
#define PULSE_US   300
#define TABLE_LEN  101

#define ISR_PRIORITY_ATOM0  10
#define ISR_PRIORITY_ATOM1  11
#define ISR_PRIORITY_ATOM2  12
#define ISR_PRIORITY_ATOM3  13

IFX_INTERRUPT(atom0_isr, 0, ISR_PRIORITY_ATOM0);
IFX_INTERRUPT(atom2_isr, 0, ISR_PRIORITY_ATOM1);
